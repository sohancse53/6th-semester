.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'Hello World'
REV DB 11 DUP(?)
.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    MOV SI, OFFSET MSG
    MOV DI, OFFSET REV
    MOV CX, 11
    ADD SI, CX
    DEC SI

REVERSE_LOOP:
    MOV AL, [SI]
    MOV [DI], AL
    DEC SI
    INC DI
    LOOP REVERSE_LOOP

    MOV SI, OFFSET REV
    MOV CX, 11

PRINT_LOOP:
    MOV DL, [SI]
    MOV AH, 02H
    INT 21H
    INC SI
    LOOP PRINT_LOOP

    MOV AH, 4CH
    INT 21H
MAIN ENDP
END MAIN
