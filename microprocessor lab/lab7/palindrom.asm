.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'LEVEL$'
PAL DB 'Palindrome$',0DH,0AH
NOTPAL DB 'Not Palindrome$',0DH,0AH
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX

    MOV CX,0
    MOV SI,0

FIND_LEN:
    MOV AL, MSG[SI]
    CMP AL,'$'
    JE LEN_FOUND
    INC CX
    INC SI
    JMP FIND_LEN

LEN_FOUND:
    DEC SI
    MOV DI,0

CHECK_LOOP:
    MOV AL, MSG[DI]
    MOV BL, MSG[SI]
    CMP AL, BL
    JNE NOT_PAL_MSG

    INC DI
    DEC SI
    CMP DI, SI
    JLE CHECK_LOOP

    MOV DX, OFFSET PAL
    MOV AH,09H
    INT 21H
    JMP EXIT_PROGRAM

NOT_PAL_MSG:
    MOV DX, OFFSET NOTPAL
    MOV AH,09H
    INT 21H

EXIT_PROGRAM:
    MOV AH,4CH
    INT 21H
MAIN ENDP
END MAIN
